        # Character settings
        self.character_speed_facter = 1
        self.character_speed_facter = 1.1

        # Ball settings
        self.ball_speed_factor = 0.5
        self.ball_width = 3
        self.ball_height = 15
        self.ball_color = 60, 60, 60
